def build():
    data = []
    for title, slug, category, pay, jobs, education, outlook_pct, outlook_desc, exposure, rationale in RAW:
        data.append({
            "title": title,
            "slug": slug,
            "category": category,
            "pay": pay,
            "jobs": jobs,
            "outlook": outlook_pct,
            "outlook_desc": outlook_desc,
            "education": education,
            "exposure": exposure,
            "exposure_rationale": rationale,
            "url": "",
        })

    # Also write intermediate files for pipeline compatibility
    occupations = []
    scores = []
    for d in data:
        occupations.append({
            "title": d["title"], "slug": d["slug"], "category": d["category"],
            "median_pay_annual": d["pay"], "num_jobs": d["jobs"],
            "education": d["education"], "outlook_pct": d["outlook"],
            "outlook_desc": d["outlook_desc"],
        })
        scores.append({
            "slug": d["slug"], "title": d["title"],
            "exposure": d["exposure"], "rationale": d["exposure_rationale"],
        })

    os.makedirs("cn", exist_ok=True)
    os.makedirs("site-cn", exist_ok=True)

    with open("cn/occupations.json", "w") as f:
        json.dump(occupations, f, ensure_ascii=False, indent=2)
    with open("cn/scores.json", "w") as f:
        json.dump(scores, f, ensure_ascii=False, indent=2)
    with open("site-cn/data.json", "w") as f:
        json.dump(data, f, ensure_ascii=False)

    total_jobs = sum(d["jobs"] for d in data if d["jobs"])
    print(f"Wrote {len(data)} occupations")
    print(f"Total jobs: {total_jobs:,} ({total_jobs/1e8:.1f}亿)")
    by_cat = {}
    for d in data:
        by_cat[d['category']] = by_cat.get(d['category'], 0) + 1
    for cat, n in sorted(by_cat.items(), key=lambda x: -x[1]):
        print(f"  {cat}: {n}")

if __name__ == "__main__":
    build()
